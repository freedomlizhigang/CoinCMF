    * 产生随机字符串
    *
    * @param    int        $length  输出长度
    * @param    string     $chars   可选的 ，默认为 0123456789
    * @return   string     字符串
    */
    public static function random($length, $chars = '0123456789') {
        $hash = '';
        $max = strlen($chars) - 1;
        for($i = 0; $i < $length; $i++) {
            $hash .= $chars[mt_rand(0, $max)];
        }
        return $hash;
    }
    //uuid生成方法（可以指定前缀）
    public static function createUuid($prefix = ""){
        $str = md5(uniqid(mt_rand(), true));
        $uuid  = substr($str,0,8) . '-';
        $uuid .= substr($str,8,4) . '-';
        $uuid .= substr($str,12,4) . '-';
        $uuid .= substr($str,16,4) . '-';
        $uuid .= substr($str,20,12);
        return $prefix . $uuid;
    }
    // 处理关键字
    public static function processKeyword($v)
    {
        // 替换特殊字符
        $v = trim(str_replace('@','',str_replace('_', '', $v)));
        $v = explode(PHP_EOL, $v);
        $tmp = '';
        foreach ($v as $k) {
            if (trim($k) != '' ) {
                $tmp .= '_'.trim($k);
            }
        }
        $tmp .= '_';
        // 处理为json
        return $tmp;
    }

